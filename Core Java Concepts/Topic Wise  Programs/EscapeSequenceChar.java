    public static void main(String[]args){
        System.out.println("Program for the Escape Sequence Characters");
        String a="SagarLawande";
        
        String b="Sagar\tLawande";
        System.out.println(a);
        System.out.println("Use of \t called as Backslach t Tab escapesequence character");
        System.out.println(b);
        System.out.println("\nUSe of \b Called as Backslash b Tab escapesequence charachter");
        String name="Sagar";
        String escape="Sag\bar";
        System.out.println(name);
        System.out.println(escape);
        System.out.println("\nuse for next line");
        String var="SagarLawande";
        String var1="Sagar\nLawande";
        System.out.println(var);
        System.out.println(var1);


    }
