public class ForEachLoop{
    public static void main(String[]args){
        System.out.println("Program for the use of for Each Loop");
        int[]arr={2,3,4,5,6,74,32,234,67,34};
        for(int num:arr)                               //data type var name//
        {                                              //the variable which can travel on whole array
            System.out.println(num);
        }
    }
}